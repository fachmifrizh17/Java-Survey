    private void ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahActionPerformed
        // TODO add your handling code here:String jenis = null;
        String jenis = null;
        if(jk1.isSelected()){jenis = "Perempuan";}else{jenis = "Laki-laki";}
        try{
            String sql = "update db_pelanggan set "
                    + "id_pelanggan=?,"
                    + "nama=?,"
                    + "jenis_kelamin=?,"
                    + "wa=?,"
                    + "email=?, "
                    + "alamat=? "
                    + "where id_pelanggan='"+idp.getText()+"'";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, idp.getText());
            stat.setString(2, nama.getText());
            stat.setString(3, jenis);
            stat.setString(4, wa.getText());
            stat.setString(5, email.getText());
            stat.setString(6, alamat.getText());
    
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data akan diubah");
            kosong();
            idp.requestFocus();
            }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal diubah"+e);
            }
            tabel();
    }//GEN-LAST:event_ubahActionPerformed

